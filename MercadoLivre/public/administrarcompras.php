    <h1>Precisa de ajuda com sua compra?</h1>
    <p>Se você está enfrentando algum problema com sua encomenda, estamos aqui para te ajudar! Confira abaixo as opções e instruções para resolver rapidamente qualquer situação relacionada ao seu pedido.</p>

    <h2>🔍 Rastreamento de pedido</h2>
    <p>Assim que seu pagamento é confirmado, enviamos o produto e você pode acompanhar o status da entrega diretamente na sua conta. Basta acessar <strong>“Minhas compras”</strong> e clicar em <strong>“Rastrear pedido”</strong> para ver onde sua encomenda está e o prazo estimado de entrega.</p>

    <h2>💳 Pagamentos</h2>
    <p>Se o pagamento ainda não foi aprovado, verifique:</p>
    <ul>
      <li>Se o cartão de crédito tem limite disponível;</li>
      <li>Se o boleto foi pago dentro do prazo;</li>
      <li>Ou se a transação foi aprovada pelo seu banco.</li>
    </ul>
    <p>Caso o pagamento não seja confirmado, o pedido será automaticamente cancelado.</p>

    <h2>❌ Cancelamentos</h2>
    <p>Você pode cancelar um pedido antes do envio ou dentro do prazo de arrependimento (7 dias após o recebimento). Para cancelar, vá até <strong>“Minhas compras” → “Detalhes do pedido” → “Cancelar compra”</strong>. O reembolso será feito no mesmo método de pagamento utilizado.</p>

    <h2>✏️ Alterações no pedido</h2>
    <p>Após a confirmação da compra, não é possível alterar o endereço ou os itens do pedido. Se precisar fazer modificações, cancele a compra e realize um novo pedido com as informações corretas.</p>
     
     <h2>🗣️ Reclamações</h2>
    <p>Se o produto não chegou, veio com defeito ou é diferente do anunciado, você pode abrir uma reclamação diretamente na sua conta. Acesse <strong>“Minhas compras” → “Problema com o pedido”</strong> e descreva o que aconteceu. Nossa equipe analisará o caso e ajudará a resolver da melhor forma.</p>

